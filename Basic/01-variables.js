//Lo nombres de las variables son case sensitive, x y X son diferentes

// var **Menos usada**

var helloWorld = "¡Hola, JavaScript!"
console.log(helloWorld)

helloWorld = "¡Hola de nuevo, JavaScript!"
console.log(helloWorld)

// let mutable

let helloWorld2 = "¡Hola, JavaScript 2!"
console.log(helloWorld2)

helloWorld2 = "¡Hola de nuevo, JavaScript 2!"
console.log(helloWorld2)

// const no se puede modificar, usar cuando la variable no va a cambiarse, ejemplo un ID, Arrays y Objetos

const helloWorld3 = "¡Hola, JavaScript 3!"
console.log(helloWorld3)

